void main() {
  var nilai = 'A';
  switch (nilai) {
    case 'A':
      print('sangat baik');
      break;
    case 'B':
      print('Baik');
      break;
    case 'C':
      print('Cukup');
      break;
    case 'D':
      print('Kurang');
      break;
    case 'E':
      print('Sangat Kurang');
      break;
    default:
      print('Data Tidak Valid');
      break;
  }
}
